"""
Ejercicio: Calcular el área de un círculo con radio dado

Lógica   : Importar librería math para obtener valor PI, definir radio, 
           usar operador multiplicación, elevado y función print()
"""

# Librerías
import math

# Variables
pi = math.pi
radio = 3

# Proceso
area = round(pi * radio ** 2, 2)

# Resultado
print("El área es", area)

