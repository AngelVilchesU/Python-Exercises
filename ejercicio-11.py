"""
Ejercicio: Calcula el área de un rectángulo, pida la base y altura al usuario

Lógica   : Pedir variables por consola mediante input(), usar operador multiplicación (*)
           y mostrar resultado con print()
"""

# Variables
base = float(input("Ingrese el ancho/base del rectángulo: "))
altura = float(input("Ingrese la altura del rectángulo: "))

# Proceso
area = base * altura

# Resultado
print("El área del rectángulo es:", area)

