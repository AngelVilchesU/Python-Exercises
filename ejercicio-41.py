"""
Ejercicio: Imprime los números del 10 al 1 en orden descendente

Lógica   : Crear variables numéricas de inicio y fin, usar ciclo para imprimir descendentemente
"""

# Variables
inicio = 1
fin = 10

# Proceso
while fin != inicio - 1:
    print(fin, " ", end="")
    fin = fin - 1

